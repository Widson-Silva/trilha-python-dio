nome = "Francisco Widson Silva Oliveira"

# Fatiamento de Strings

print(nome[0]) # Primeiro indice
print(nome[-1]) # Ultimo indice
print(nome[:9]) # Pega do primeiro indice (0) até o 9
print(nome[10:]) # Pega o indice 10 até o ultimo
print(nome[10:16]) # Pega o indicie 10 até o 16
print(nome[10:16:2]) # Pega o indice 10 ao 16 pulando de 2 em 2
print(nome[:]) # Mostra todo o indice
print(nome[::-1]) # Espelha o indice